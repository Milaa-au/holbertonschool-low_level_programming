#include "3-calc.h"

/**
* op_add - Addtion
* @a: Entier a additionner
* @b: Entier a additionner
*
* Return: Entier (int)
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Soustraction
* @a: Entier a soustraire
* @b: Entier a soustraire
*
* Return: Entier (int)
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - Multiplication
* @a: Entier a multiplier
* @b: Entier a multiplier
*
* Return: Entier (int)
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - Division
* @a: Entier a diviser
* @b: Entier a diviser
*
* Return: Entier (int)
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Modulo
* @a: Entier a modulo
* @b: Entier a modulo
*
* Return: Entier (int)
*/

int op_mod(int a, int b)
{
	return (a % b);
}
